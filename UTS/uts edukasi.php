				<center><h1>JENIS SAMPAH & CARA MENGELOLANYA</h1>
				<img src="edukasi1.png" width="500" height="400"></center>
				<p align="justify">
				Sebagai manusia, kita sudah diajarkan sejak dini tentang pentingnya membuang sampah pada tempatnya. Kesadaran ini memang harus dipupuk sejak kecil karena dampak sampah pada kehidupan sehari-hari bisa memengaruhi kesehatan dan lingkungan jika tidak dikelola dengan baik. Namun, kamu harus mengetahui terlebih dahulu berbagai jenis sampah yang dihasilkan manusia.<br><br>

				Sebelum memulai, kita harus mengetahui terlebih dahulu apa sih sebenarnya sampah itu. Pengertian sampah menurut KBBI adalah “barang yang dibuang oleh pemiliknya karena tidak terpakai lagi atau tidak dinginkan lagi, misalnya kotoran, kaleng minuman, daun-daunan, kertas, dan lain-lain.”.
				<br><br><br>
				
				<h2>Jenis Sampah Berdasarkan Sumber</h2>
				<p align="justify"> Berdasarkan sumber, sampah dibagi menjadi 6 jenis, meliputi:<br><br>

				1. Sampah dari manusia<br>
				2. Sampah dari alam<br>
				3. Sampah konsumsi<br>
				4. Sampah nuklir/limbah radioaktif<br>
				5. Sampah industri<br>
				6. Sampah pertambangan</p>
				<br><br><br><br><br><br><br><br><br>

				<h2>Jenis Sampah Berdasarkan Sifat</h2>
				<center><img src="edukasi2.png" width="500" height="400"></center>
				
				<h3>1. Sampah Organik</h3>
				Sampah organik merupakan sampah yang bisa terurai atau diolah menjadi pupuk kompos. Jenis sampah yang dianggap sebagai organik mencakup sisa makanan, daun kering, sayuran, dan sebagainya.

				<h3>2. Sampah Anorganik</h3>
				Sampah anorganik adalah sampah yang sulit untuk membusuk dan tidak bisa terurai. Jika tidak dikelola dengan baik, maka sampah ini bisa merusak ekosistem hewan dan manusia. Namun, keberadaan sampah ini bisa diakali dengan cari didaur ulang. Contoh sampah anorganik meliputi plastik, karton, logam, dan sebagainya.

				<h3>3. Sampah B3</h3>
				Sampah jenis ini biasanya merupakan sisa dari pengolahan bahan kimia yang berbahaya. Jenis sampah B3 sendiri meliputi:<br><br>

				- Sumber tidak spesifik: Limbah yang berasal dari kegiatan pemeliharaan alat, pelarutan kerak, mencuci, dan lain-lain.<br>
				- Sumber spesifik: Limbah yang berasal dari proses industri (kegiatan utama).<br>
				- Sumber lain: Limbah yang berasal dari sumber tak terduga seperti produk yang kedaluwarsa, sisa kemasan, dan baungan produk yang tidak memenuhi spesifikasi.
				
				<br><br><br>
				<h2>Jenis Sampah Berdasarkan Bentuk</h2>
				<center><img src="edukasi3.png" width="500" height="500"></center>
				
				<h3>1. Sampah Padat</h3>
				Sampah padat adalah sampah selain kotoran manusia, urine, dan sampah cair. Sampah organik dan anorganik bisa dikelompokan ke dalam jenis sampah yang satu ini. Berdasarkan kemampuan ketika diurai oleh alam, sampah ini dibagi kembali ke dalam 2 kategori, yakni biodegradable dan non-biodegradable.

				<h3>2. Sampah Cair</h3>
				Sampah cair dibagi menjadi 2 kategori, yakni limbah hitam dan limbah rumah tangga. Limbah hitam dalah sampah yang dihasilkan dari toilet. Sampah yang satu ini mengandung patogen yang sangat berbahaya untuk manusia. Sedangkan limbah rumah tangga adalah sampah yang dihasilkan dari dapur, kamar mandi, dan tempat cucian. Berbeda dengan limbah hitam, keberadaan kandungan patogen pada sampah ini masih berupa kemungkinan.
				
				<h2>Praktikkan Prinsip 4R untuk Mengolah Sampah di Rumah</h2>
				<center><img src="edukasi.png" width="500" height="500"></center>
				
				Dari segala jenis sampah yang ada di atas, kamu bisa mengolahnya dengan prinsip 4R.<br>
				4R adalah singkatan dari:<br><br>
				- Replace (mengganti).<br>
				- Reduce (mengurangi).<br>
				- Re-use (memakai).<br>
				- Recycle (daur ulang).<br>
				Untuk lebih jelasnya, simak yuk ulasannya di bawah ini.<br><br>

				<h3>1. Replace (mengganti)</h3>
				Usahakan untuk menggunakan barang ramah lingkungan yang bisa digunakan lebih dari sekali pemakaian. Hal ini bisa dilakukan dengan cara-cara sederhana seperti:<br> 
				- Ganti kantong keresek dengan tas belanja. <br>
				- Hindari penggunaan styrofoam. <br><br>
				
				<h3>2. Reduce (mengurangi)</h3>
				Mulailah untuk mengurangi produksi sampah yang dihasilkan oleh diri sendiri. Untuk melakukannya, kamu bisa mepraktikkan cara berikut:<br>
				- Bawa tas belanja sendiri untuk mengurangi sampah kantong plastik.<br>
				- Membawa botol minuman daripada membeli minuman dalam kemasan. <br><br>

				<h3>3. Re-use (memakai)</h3>
				Kamu ternyata bisa menggunakan kembali barang yang sudah tidak terpakai, lho. Contohnya adalah seperti ini:<br>
				- Menggunakan plastik bekas belanja untuk pembungkus di kemudian hari.<br>
				- Memakai kaleng bekas sebagai pot tanaman<br>
				- Memakai pakaian bekas sebagai lap, kerajinan tangan, dan lainnya.<br><br>
				
				<h3>4. Recycle (daur ulang)</h3>
				Teknologi dan penanganan yang khusus dibutuhkan untuk mendaur ulang dampah tertentu. Inilah beberapa sampah yang bisa didaur ulang:<br>
				- Kertas seperti majalah dan surat kabar bekas.<br>
				- Logam seperti kaleng dan sendok bekas.<br>
				- Kaca seperti botol dan gelas bekas.
				</p>
